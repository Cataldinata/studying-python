# ---- Operadores de Atribuição
## Como o nome já diz, esses operadores servem para atribuir valores a uma variável.

# Atribuição Simples (=) 
a = 3
print(a) # mostrará 3

# Incremento (+=)
a += 3
print(a) # mostrará 6

# Decremento (-=)   
a -= 1
print(a) # mostrará 5

# Atribuição com Multiplicação (*=)
a *= 2
print(a) # mostrará 10

# Atribuição com Divisão Inteira (//=)
a //= 2
print(a) # mostrará 5 (int)

# Atribuição com Divisão (/=)
a /= 1
print(a) # mostrará 5.0 (float)

# Atribuição com Módulo (%=)
a %= 2
print(a) # mostrará 0

# Atribuição com Exponenciação (**=)
a = 2
a **= 2
print(a) # mostrará 4

# Atribuição com Radiciação (**= n/n)
a **= 1/2
print(a) # mostrará 2