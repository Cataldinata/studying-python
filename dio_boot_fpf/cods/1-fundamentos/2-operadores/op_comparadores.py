# --- Operadores de Comparação
# Como o nome já diz, são utilizados para comparar valores. Retornarão True ou False, de acordo com a condição.

## Operador Maior Que (>)
um = 1
dois = 2
print(um > dois) # Retornará false

## Operador Menor Que (<)   
print(um < dois) # retornará true

## Operador Igual (==)
print(um == dois) # retornará false

## Operador Não Igual ou Diferente (!=)
print(um != dois) #retornará true

## Operador Maior Que ou Igual (>=)
print(um >= dois) # retornará false

## Operador menor Que ou Igual (<=)
print(um <= dois) # retornará true
