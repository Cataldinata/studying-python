# VARIÁVEIS - valor se altera durante o código
age = 23
name = 'Natã'
print(f'Meu nome é {name} e eu tenho {age} ano(s) de idade.')

age, name = (25, 'Guilherme')
print(f'Meu nome is {name} e eu tenho {age} year(s) old.')


# CONSTANTE - o valor permance o mesmo durante o código
# por convenção, as constantes em Python são declaradas com todas as letras em maiúsculo. O Python não tem um tipo constente igual a outras linguagens

VALORPI = 3.14

print(3 * VALORPI)


